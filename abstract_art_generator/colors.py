"""Color palettes and utilities."""
def parse_hex_color(s):
    s = s.lstrip('#')
    if len(s) == 3:
        s = ''.join([c*2 for c in s])
    r = int(s[0:2], 16)
    g = int(s[2:4], 16)
    b = int(s[4:6], 16)
    return (r, g, b)

